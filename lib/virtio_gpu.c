/**
 * virtio_gpu_draw_char - Draw a single character to the framebuffer
 * @x, y: Top-left pixel position
 * @c: Character to draw (ASCII 0x20-0x7E)
 * @fg: Foreground color (32-bit BGRX)
 * @bg: Background color (32-bit BGRX)
 */
void virtio_gpu_draw_char(uint32_t x, uint32_t y, char c,
                          uint32_t fg, uint32_t bg)
{
    if (!g_gpu.present || !g_gpu.fb)
        return;

    if (c < ' ' || c > '~')
        c = '?';

    if (x + 8 > g_gpu.width || y + 16 > g_gpu.height)
        return;

    const uint8_t *glyph = font_8x16[(int)c];
    uint32_t stride = g_gpu.stride;
    uint8_t *fb = g_gpu.fb;

    for (int row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        uint32_t *line = (uint32_t *)(fb + (y + row) * stride + x * 4);
        for (int col = 0; col < 8; col++) {
            line[col] = (bits & (0x80 >> col)) ? fg : bg;
        }
    }
}
