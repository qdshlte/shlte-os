/*
 * virtio_gpu.c - VirtIO-GPU MMIO Driver
 *
 * Detects and initializes a virtio-gpu device on the QEMU ARM64 virt
 * machine, creates a framebuffer, and provides basic drawing primitives.
 *
 * The GPU is detected by scanning virtio MMIO slots starting from
 * 0x0A000000 for device ID 16 (VIRTIO_ID_GPU).
 */

#include <shlte/types.h>
#include <shlte/printk.h>
#include <shlte/string.h>
#include <shlte/mm.h>
#include <shlte/virtio_gpu.h>

/* ============================================================
 * MMIO Helpers
 * ============================================================ */

typedef volatile uint32_t vu32;

static inline uint32_t mmio_read32(vu32 *addr)
{
    return *addr;
}

static inline void mmio_write32(vu32 *addr, uint32_t val)
{
    *addr = val;
}

/* ============================================================
 * Virtqueue layout (placed at fixed physical address)
 * ============================================================ */

#define VQ_BASE         0x41000000UL    /* Above kernel heap */
#define VQ_DESC         0x41000000UL
#define VQ_AVAIL        0x41001000UL
#define VQ_USED         0x41002000UL
#define VQ_NUM          64

static virtq_desc_t  *vq_desc  = (virtq_desc_t *)VQ_DESC;
static virtq_avail_t *vq_avail = (virtq_avail_t *)VQ_AVAIL;
static virtq_used_t  *vq_used  = (virtq_used_t *)VQ_USED;

static uint16_t vq_next_idx = 0;
static uint16_t vq_last_used = 0;

/* ============================================================
 * GPU global state
 * ============================================================ */

gpu_state_t g_gpu;

/* ============================================================
 * Virtqueue operations
 * ============================================================ */

/**
 * vq_submit - Submit a chain of descriptors to the virtqueue
 * @desc_count: Number of descriptors in the chain
 *
 * Returns after the device has consumed the descriptors.
 * Uses polling (no interrupt-driven I/O yet).
 */
static void vq_submit(vu32 *mmio, int desc_count)
{
    uint16_t head = vq_next_idx;

    /* Add to available ring */
    vq_avail->ring[vq_avail->idx % VQ_NUM] = head;
    __asm__ volatile("dmb sy");
    vq_avail->idx++;

    /* Notify device */
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_NOTIFY / 4), 0);

    /* Poll for completion */
    for (volatile int timeout = 0; timeout < 50000000; timeout++) {
        __asm__ volatile("dmb sy");
        if (vq_used->idx != vq_last_used) {
            vq_last_used = vq_used->idx;
            break;
        }
    }

    vq_next_idx = (vq_next_idx + desc_count) % VQ_NUM;
}

/**
 * vq_setup_desc - Configure a descriptor in the chain
 */
static int vq_setup_desc(int idx, uint64_t phys_addr, uint32_t len, int flags)
{
    vq_desc[idx].addr  = phys_addr;
    vq_desc[idx].len   = len;
    vq_desc[idx].flags = (uint16_t)flags;
    vq_desc[idx].next  = 0;
    return idx;
}

/* ============================================================
 * Virtio-gpu command submission
 * ============================================================ */

/* Statically allocated buffers for command and response */
static virtio_gpu_cmd_resource_create_2d_t    __attribute__((aligned(16))) g_cmd_create;
static virtio_gpu_resp_display_info_t         __attribute__((aligned(16))) g_resp_info;
static virtio_gpu_ctrl_hdr_t                  __attribute__((aligned(16))) g_resp_hdr;
static virtio_gpu_cmd_resource_attach_backing_t __attribute__((aligned(16))) g_cmd_attach;
static virtio_gpu_mem_entry_t                 __attribute__((aligned(16))) g_mem_entry;
static virtio_gpu_cmd_set_scanout_t           __attribute__((aligned(16))) g_cmd_scanout;
static virtio_gpu_cmd_resource_flush_t        __attribute__((aligned(16))) g_cmd_flush;
static virtio_gpu_cmd_transfer_to_host_2d_t   __attribute__((aligned(16))) g_cmd_transfer;

/**
 * gpu_send_get_display_info - Query display dimensions
 */
static int gpu_send_get_display_info(vu32 *mmio, uint32_t *width, uint32_t *height)
{
    memset(&g_cmd_create, 0, sizeof(g_cmd_create));
    g_cmd_create.hdr.type = VIRTIO_GPU_CMD_GET_DISPLAY_INFO;

    memset(&g_resp_info, 0, sizeof(g_resp_info));

    int d0 = vq_setup_desc(vq_next_idx, (uint64_t)&g_cmd_create,
                            sizeof(g_cmd_create), VIRTQ_DESC_F_NEXT);
    int d1 = vq_setup_desc((vq_next_idx + 1) % VQ_NUM, (uint64_t)&g_resp_info,
                            sizeof(g_resp_info), VIRTQ_DESC_F_WRITE);
    vq_desc[d0].next = (uint16_t)d1;

    vq_submit(mmio, 2);

    if (g_resp_info.hdr.type != VIRTIO_GPU_RESP_OK_DISPLAY_INFO) {
        printk("[GPU] GET_DISPLAY_INFO failed: resp type=0x%x\n",
               g_resp_info.hdr.type);
        return -1;
    }

    *width  = g_resp_info.pmodes[0].width;
    *height = g_resp_info.pmodes[0].height;
    printk("[GPU] Display: %ux%u\n", *width, *height);
    return 0;
}

/**
 * gpu_create_framebuffer - Allocate a 2D resource and attach backing memory
 */
static int gpu_create_framebuffer(vu32 *mmio, uint32_t width, uint32_t height,
                                   uint8_t *fb_phys, uint32_t fb_size)
{
    uint32_t rid = 1;

    /* Step 1: CREATE_2D */
    memset(&g_cmd_create, 0, sizeof(g_cmd_create));
    g_cmd_create.hdr.type     = VIRTIO_GPU_CMD_RESOURCE_CREATE_2D;
    g_cmd_create.resource_id  = rid;
    g_cmd_create.format       = VIRTIO_GPU_FORMAT_B8G8R8X8_UNORM;
    g_cmd_create.width        = width;
    g_cmd_create.height       = height;

    memset(&g_resp_hdr, 0, sizeof(g_resp_hdr));

    int d0 = vq_setup_desc(vq_next_idx, (uint64_t)&g_cmd_create,
                            sizeof(g_cmd_create), VIRTQ_DESC_F_NEXT);
    int d1 = vq_setup_desc((vq_next_idx + 1) % VQ_NUM, (uint64_t)&g_resp_hdr,
                            sizeof(g_resp_hdr), VIRTQ_DESC_F_WRITE);
    vq_desc[d0].next = (uint16_t)d1;

    vq_submit(mmio, 2);

    if (g_resp_hdr.type != VIRTIO_GPU_RESP_OK_NODATA) {
        printk("[GPU] RESOURCE_CREATE_2D failed: resp=0x%x\n", g_resp_hdr.type);
        return -1;
    }

    /* Step 2: ATTACH_BACKING */
    memset(&g_cmd_attach, 0, sizeof(g_cmd_attach));
    g_cmd_attach.hdr.type     = VIRTIO_GPU_CMD_RESOURCE_ATTACH_BACKING;
    g_cmd_attach.resource_id  = rid;
    g_cmd_attach.nr_entries   = 1;

    memset(&g_mem_entry, 0, sizeof(g_mem_entry));
    g_mem_entry.addr   = (uint64_t)fb_phys;
    g_mem_entry.length = fb_size;

    memset(&g_resp_hdr, 0, sizeof(g_resp_hdr));

    d0 = vq_setup_desc(vq_next_idx, (uint64_t)&g_cmd_attach,
                        sizeof(g_cmd_attach), VIRTQ_DESC_F_NEXT);
    int d1a = vq_setup_desc((vq_next_idx + 1) % VQ_NUM, (uint64_t)&g_mem_entry,
                             sizeof(g_mem_entry), VIRTQ_DESC_F_NEXT);
    int d2 = vq_setup_desc((vq_next_idx + 2) % VQ_NUM, (uint64_t)&g_resp_hdr,
                            sizeof(g_resp_hdr), VIRTQ_DESC_F_WRITE);
    vq_desc[d0].next  = (uint16_t)d1a;
    vq_desc[d1a].next = (uint16_t)d2;

    vq_submit(mmio, 3);

    if (g_resp_hdr.type != VIRTIO_GPU_RESP_OK_NODATA) {
        printk("[GPU] ATTACH_BACKING failed: resp=0x%x\n", g_resp_hdr.type);
        return -1;
    }

    /* Step 3: SET_SCANOUT */
    memset(&g_cmd_scanout, 0, sizeof(g_cmd_scanout));
    g_cmd_scanout.hdr.type      = VIRTIO_GPU_CMD_SET_SCANOUT;
    g_cmd_scanout.scanout_id    = 0;
    g_cmd_scanout.resource_id   = rid;
    g_cmd_scanout.r_width       = width;
    g_cmd_scanout.r_height      = height;

    memset(&g_resp_hdr, 0, sizeof(g_resp_hdr));

    d0 = vq_setup_desc(vq_next_idx, (uint64_t)&g_cmd_scanout,
                        sizeof(g_cmd_scanout), VIRTQ_DESC_F_NEXT);
    d1 = vq_setup_desc((vq_next_idx + 1) % VQ_NUM, (uint64_t)&g_resp_hdr,
                        sizeof(g_resp_hdr), VIRTQ_DESC_F_WRITE);
    vq_desc[d0].next = (uint16_t)d1;

    vq_submit(mmio, 2);

    if (g_resp_hdr.type != VIRTIO_GPU_RESP_OK_NODATA) {
        printk("[GPU] SET_SCANOUT failed: resp=0x%x\n", g_resp_hdr.type);
        return -1;
    }

    printk("[GPU] Framebuffer ready: %ux%u, resource=%u\n", width, height, rid);
    return 0;
}

/* ============================================================
 * Public API
 * ============================================================ */

/**
 * virtio_gpu_init - Detect and initialize the virtio-gpu device
 *
 * Scans MMIO slots for device ID 16 (VIRTIO_ID_GPU), negotiates
 * features, creates a framebuffer, and sets the scanout.
 *
 * Returns 0 on success, -1 if no GPU found or init fails.
 */
int virtio_gpu_init(void)
{
    vu32 *mmio = NULL;
    uint64_t base = 0x0A000000UL;
    int found = 0;

    memset(&g_gpu, 0, sizeof(g_gpu));

    /* Scan MMIO slots for a virtio-gpu device */
    for (int slot = 0; slot < 32; slot++) {
        vu32 *addr = (vu32 *)(base + slot * 0x200);
        uint32_t magic = mmio_read32(addr + (VIRTIO_MMIO_MAGIC_VALUE / 4));

        if (magic != 0x74726976)  /* "virt" in little-endian */
            continue;

        uint32_t dev_id = mmio_read32(addr + (VIRTIO_MMIO_DEVICE_ID / 4));
        if (dev_id == VIRTIO_GPU_DEVICE_ID) {
            mmio = addr;
            found = 1;
            printk("[GPU] Found virtio-gpu at MMIO slot %d (0x%lx)\n", slot, base + slot * 0x200);
            break;
        }
    }

    if (!found) {
        printk("[GPU] No virtio-gpu device found\n");
        return -1;
    }

    /* Initialize the device */
    mmio_write32(mmio + (VIRTIO_MMIO_STATUS / 4), 0);
    mmio_write32(mmio + (VIRTIO_MMIO_STATUS / 4), VIRTIO_STATUS_ACKNOWLEDGE);
    mmio_write32(mmio + (VIRTIO_MMIO_STATUS / 4),
                 VIRTIO_STATUS_ACKNOWLEDGE | VIRTIO_STATUS_DRIVER);

    /* No special features needed for basic framebuffer */
    mmio_write32(mmio + (VIRTIO_MMIO_DRIVER_FEATURES_SEL / 4), 0);
    mmio_write32(mmio + (VIRTIO_MMIO_DRIVER_FEATURES / 4), 0);

    mmio_write32(mmio + (VIRTIO_MMIO_STATUS / 4),
                 VIRTIO_STATUS_ACKNOWLEDGE | VIRTIO_STATUS_DRIVER |
                 VIRTIO_STATUS_FEATURES_OK);

    /* Set up virtqueue */
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_SEL / 4), 0);
    uint32_t qmax = mmio_read32(mmio + (VIRTIO_MMIO_QUEUE_NUM_MAX / 4));
    if (qmax > VQ_NUM) qmax = VQ_NUM;
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_NUM / 4), qmax);

    /* Point queue at our pre-allocated descriptor area */
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_DESC_LOW / 4), (uint32_t)VQ_DESC);
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_DESC_HIGH / 4), 0);
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_DRIVER_LOW / 4), (uint32_t)VQ_AVAIL);
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_DRIVER_HIGH / 4), 0);
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_DEVICE_LOW / 4), (uint32_t)VQ_USED);
    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_DEVICE_HIGH / 4), 0);

    mmio_write32(mmio + (VIRTIO_MMIO_QUEUE_READY / 4), 1);

    mmio_write32(mmio + (VIRTIO_MMIO_STATUS / 4),
                 VIRTIO_STATUS_ACKNOWLEDGE | VIRTIO_STATUS_DRIVER |
                 VIRTIO_STATUS_FEATURES_OK | VIRTIO_STATUS_DRIVER_OK);

    /* Query display info */
    uint32_t width, height;
    if (gpu_send_get_display_info(mmio, &width, &height) != 0)
        return -1;

    if (width == 0 || height == 0) {
        printk("[GPU] Invalid display dimensions\n");
        return -1;
    }

    if (width > MAX_FB_WIDTH)  width  = MAX_FB_WIDTH;
    if (height > MAX_FB_HEIGHT) height = MAX_FB_HEIGHT;

    /* Allocate framebuffer (physically contiguous) */
    uint32_t fb_size = width * height * 4;
    uint8_t *fb = (uint8_t *)kmalloc_simple(fb_size);
    if (!fb) {
        printk("[GPU] Cannot allocate framebuffer (%u bytes)\n", fb_size);
        return -1;
    }
    memset(fb, 0x30, fb_size);  /* Dark grey background */

    /* Create the GPU resource and attach the framebuffer */
    if (gpu_create_framebuffer(mmio, width, height, fb, fb_size) != 0) {
        printk("[GPU] Framebuffer setup failed\n");
        return -1;
    }

    g_gpu.present      = 1;
    g_gpu.width        = width;
    g_gpu.height       = height;
    g_gpu.stride       = width * 4;
    g_gpu.fb           = fb;
    g_gpu.resource_id  = 1;
    g_gpu.mmio_base    = (uint64_t)mmio;

    printk("[GPU] Initialized: %ux%u, fb=0x%lx\n",
           width, height, (uint64_t)fb);
    return 0;
}

/**
 * virtio_gpu_flush - Flush the full framebuffer to the display
 */
int virtio_gpu_flush(void)
{
    if (!g_gpu.present) return -1;

    vu32 *mmio = (vu32 *)g_gpu.mmio_base;

    memset(&g_cmd_transfer, 0, sizeof(g_cmd_transfer));
    g_cmd_transfer.hdr.type      = VIRTIO_GPU_CMD_TRANSFER_TO_HOST_2D;
    g_cmd_transfer.resource_id   = g_gpu.resource_id;
    g_cmd_transfer.r_x           = 0;
    g_cmd_transfer.r_y           = 0;
    g_cmd_transfer.r_width       = g_gpu.width;
    g_cmd_transfer.r_height      = g_gpu.height;
    g_cmd_transfer.offset        = 0;

    memset(&g_resp_hdr, 0, sizeof(g_resp_hdr));

    int d0 = vq_setup_desc(vq_next_idx, (uint64_t)&g_cmd_transfer,
                            sizeof(g_cmd_transfer), VIRTQ_DESC_F_NEXT);
    int d1 = vq_setup_desc((vq_next_idx + 1) % VQ_NUM, (uint64_t)&g_resp_hdr,
                            sizeof(g_resp_hdr), VIRTQ_DESC_F_WRITE);
    vq_desc[d0].next = (uint16_t)d1;

    vq_submit(mmio, 2);

    /* Then flush */
    memset(&g_cmd_flush, 0, sizeof(g_cmd_flush));
    g_cmd_flush.hdr.type       = VIRTIO_GPU_CMD_RESOURCE_FLUSH;
    g_cmd_flush.resource_id    = g_gpu.resource_id;
    g_cmd_flush.r_x            = 0;
    g_cmd_flush.r_y            = 0;
    g_cmd_flush.r_width        = g_gpu.width;
    g_cmd_flush.r_height       = g_gpu.height;

    memset(&g_resp_hdr, 0, sizeof(g_resp_hdr));

    d0 = vq_setup_desc(vq_next_idx, (uint64_t)&g_cmd_flush,
                        sizeof(g_cmd_flush), VIRTQ_DESC_F_NEXT);
    d1 = vq_setup_desc((vq_next_idx + 1) % VQ_NUM, (uint64_t)&g_resp_hdr,
                        sizeof(g_resp_hdr), VIRTQ_DESC_F_WRITE);
    vq_desc[d0].next = (uint16_t)d1;

    vq_submit(mmio, 2);

    return 0;
}

/**
 * virtio_gpu_fill_rect - Fill a rectangle with a solid color
 * @x, y: Top-left corner
 * @w, h: Width and height
 * @color: 32-bit BGRX color (0x00RRGGBB in BGRA layout)
 */
void virtio_gpu_fill_rect(uint32_t x, uint32_t y, uint32_t w,
                          uint32_t h, uint32_t color)
{
    if (!g_gpu.present || !g_gpu.fb)
        return;

    if (x >= g_gpu.width || y >= g_gpu.height)
        return;
    if (x + w > g_gpu.width)  w = g_gpu.width - x;
    if (y + h > g_gpu.height) h = g_gpu.height - y;

    uint8_t *fb = g_gpu.fb;
    uint32_t stride = g_gpu.stride;

    for (uint32_t row = y; row < y + h; row++) {
        uint32_t *line = (uint32_t *)(fb + row * stride + x * 4);
        for (uint32_t col = 0; col < w; col++) {
            line[col] = color;
        }
    }
}

